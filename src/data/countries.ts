// Lookup map for O(1) access by code
const COUNTRY_MAP = new Map<string, CountryData>(
  COUNTRIES.map(c => [c.code, c])
);

/** Get a country by its ISO code */
export function getCountryByCode(code: string): CountryData | undefined {
  return COUNTRY_MAP.get(code.toUpperCase());
}

/** Get the centroid coordinates for a country */
export function getCountryCentroid(code: string): { lat: number; lng: number } | undefined {
  const country = COUNTRY_MAP.get(code.toUpperCase());
  if (!country) return undefined;
  return { lat: country.lat, lng: country.lng };
}

/** Search countries by name (case-insensitive prefix/substring match) */
export function searchCountries(query: string): CountryData[] {
  if (!query.trim()) return COUNTRIES;
  const q = query.toLowerCase().trim();
  return COUNTRIES.filter(c => c.name.toLowerCase().includes(q));
}
